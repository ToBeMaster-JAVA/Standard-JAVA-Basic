class EX3_6 {
    public static void main(String[] args) {
        int x = 10;
        int y = 4;

        System.out.printf("%d + %d = %d%n", x, y, x + y);
        System.out.printf("%d - %d = %d%n", x, y, x - y);
        System.out.printf("%d * %d = %d%n", x, y, x * y);
        System.out.printf("%d / %d = %d%n", x, y, x / y);
        System.out.printf("%d / %f = %f%n", x, (float)y, x / (float)y);
    }
}