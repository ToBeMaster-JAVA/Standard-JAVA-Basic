public class Ex2_4 {
    public static void main(String args[]) {
        // 선언하는 방법
        // [변수타입] [변수이름] ;
        /*
        int x ; // 선언
        x = 5 ; // 대입
        */

        int x = 5;// 선언 & 대입
        System.out.println(x) ;

        x = 10;
        System.out.println(x) ; // 기존값 5는 지워지고 10 새롭게 대입됨
    }
}
