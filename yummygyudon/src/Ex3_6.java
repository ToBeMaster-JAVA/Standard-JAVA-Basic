public class Ex3_6 {
    public static void main(String[] args) {
        int a = 5 ;
        int b = 2 ;

        System.out.printf("%d + %d = %d %n", a, b, a+b) ;
        System.out.printf("%d - %d = %d %n", a, b, a-b) ;
        System.out.printf("%d * %d = %d %n", a, b, a*b) ;
        System.out.printf("%d / %d = %d %n", a, b, a/b) ;
        System.out.printf("%d / %f = %f %n", a, (float)b, a/(float)b) ;
        System.out.printf("%f / %d = %f %n", (float)a, b, (float)a/b) ;
    }
}
